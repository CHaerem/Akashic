interface ChevronIconProps extends IconProps {
  direction?: 'left' | 'right' | 'up' | 'down';
}

export const ChevronIcon = ({ direction = 'left', size = 14, className }: ChevronIconProps) => {
  // Base SVG points LEFT, rotate to get other directions
  const rotations = {
    left: 0,
    right: 180,
    up: -90,
    down: 90,
  };

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2.5"
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      style={{ transform: rotations[direction] ? `rotate(${rotations[direction]}deg)` : undefined }}
    >
      <polyline points="15 18 9 12 15 6" />
    </svg>
  );
};

